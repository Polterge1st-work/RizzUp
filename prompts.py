def build_personalization_block(gender: str | None, partner_gender: str | None, case_style: str | None) -> str:
    """
    Собирает блок с данными о пользователе для вставки в начало системного промта.
    """
    lines = []

    if gender == "male":
        lines.append("Пользователь, от имени которого ты пишешь варианты ответа — парень. Пиши в мужском роде там где это нужно по смыслу.")
    elif gender == "female":
        lines.append("Пользователь, от имени которого ты пишешь варианты ответа — девушка. Пиши в женском роде там где это нужно по смыслу.")

    if partner_gender == "male":
        lines.append("Собеседник, которому пишет пользователь — парень. Обращайся к нему в мужском роде.")
    elif partner_gender == "female":
        lines.append("Собеседник, которому пишет пользователь — девушка. Обращайся к ней в женском роде.")

    if not lines:
        return ""

    return "════════ ДАННЫЕ О ПОЛЬЗОВАТЕЛЕ ════════\n\n" + "\n".join(lines) + "\n\n"

